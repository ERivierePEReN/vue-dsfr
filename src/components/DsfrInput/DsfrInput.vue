<script>
import { defineComponent } from 'vue'

import { getRandomId } from '../../utils/random-utils.js'

export default defineComponent({
  name: 'DsfrInput',

  inheritAttrs: false,

  props: {
    id: {
      type: String,
      default () {
        return getRandomId('basic', 'input')
      },
    },
    descriptionId: {
      type: String,
      default: undefined,
    },
    label: {
      type: String,
      default: '',
    },
    hint: {
      type: String,
      default: '',
    },
    wrapperClass: {
      type: String,
      default: '',
    },
    isWithWrapper: Boolean,
    labelVisible: Boolean,
    modelValue: {
      type: String,
      default: '',
    },
    isInvalid: Boolean,
    isValid: Boolean,
    isTextarea: Boolean,
  },

  emits: ['update:modelValue', 'blur', 'focus', 'keydown'],

  computed: {
    isComponent () {
      return this.isTextarea ? 'textarea' : 'input'
    },
    wrapper () {
      return this.isWithWrapper || this.$attrs.type === 'date' || this.wrapperClass
    },
  },
})
</script>

<template>
  <label
    :class="{
      'fr-label': true,
      'invisible': !labelVisible,
    }"
    :for="id"
  >
    {{ label }}

    <!-- @slot Slot pour indiquer que le champ est obligatoire. Par défaut, met une astérisque si `required` est à true (dans un `<span class="required">`) -->
    <slot name="required-tip">
      <span
        v-if="$attrs.required"
        class="required"
      >&nbsp;*</span>
    </slot>

    <span
      v-if="hint"
      class="fr-hint-text"
    >{{ hint }}</span>
  </label>

  <component
    :is="isComponent"
    v-if="!wrapper"
    :id="id"
    class="fr-input"
    :class="{
      'fr-input--error': isInvalid,
      'fr-input--valid': isValid,
    }"
    :value="modelValue"
    v-bind="$attrs"
    :aria-aria-describedby="descriptionId || undefined"
    @input="$emit('update:modelValue', $event.target.value)"
    @blur="$emit('blur', $event)"
    @focus="$emit('focus', $event)"
    @keydown="$emit('keydown', $event)"
  />

  <div
    v-else
    :class="[
      {
        'fr-input-wrap': isWithWrapper || $attrs.type === 'date',
        'fr-fi-calendar-line': $attrs.type === 'date',
      },
      wrapperClass,
    ]"
  >
    <component
      :is="isComponent"
      :id="id"
      class="fr-input"
      :class="{
        'fr-input--error': isInvalid,
        'fr-input--valid': isValid,
      }"
      :value="modelValue"
      v-bind="$attrs"
      :aria-aria-describedby="descriptionId || undefined"
      @input="$emit('update:modelValue', $event.target.value)"
      @blur="$emit('blur', $event)"
      @focus="$emit('focus', $event)"
      @keydown="$emit('keydown', $event)"
    />
  </div>
</template>

